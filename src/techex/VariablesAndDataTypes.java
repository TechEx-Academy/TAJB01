package techex;

public class VariablesAndDataTypes {
    public static void main(String[] args) {

        // Declare variable
        int a;

        // Initialize variable
        a = 5;

        // Both declare and initializing in one line
        int b = 10;


        // Primitive data types
        byte byteVar;
        short shortVar;
        int intVar;
        long longVar;
        float floatVar;
        double doubleVar;
        char charVar;
        boolean boolVar;

        // Non primitive data types
        String s = "TechEx";


    }
}
